@dataclass(frozen=True)















class FilterConfig:















    db_path: Path















    name_dedupe_rejection_csv: Path















    temp_kept_csv: Path | None







    temp_export_artist_names: tuple[str, ...]











































def ensure_utf8_stdout() -> None:















    if hasattr(sys.stdout, "reconfigure"):















        sys.stdout.reconfigure(encoding="utf-8")















































def normalize_song_name(value: str) -> str:















    return normalize_song_title_identity(value)















































def connect(db_path: Path) -> sqlite3.Connection:















    if not db_path.exists():















        raise FileNotFoundError(f"Database does not exist: {db_path}")















    connection = sqlite3.connect(db_path)















    connection.row_factory = sqlite3.Row















    connection.execute("PRAGMA foreign_keys = ON")















    return connection















































def fetch_song_rows(connection: sqlite3.Connection, where_sql: str = "", params: tuple[Any, ...] = ()) -> list[dict[str, Any]]:















    sql = f"""















        SELECT















            s.mid AS song_mid,















            s.id AS song_id,















            s.name AS song_name,















            s.title AS song_title,















            s.language AS song_language,















            s.album_mid,















            a.id AS album_id,















            a.name AS album_name,















            a.albumType AS album_type,















            a.publishDate AS album_publish_date,















            s.raw_json_path,















            s.raw_page,















            s.raw_row_index,















            (















                SELECT group_concat(ss.singer_order || ':' || ss.singer_mid || ':' || artists.name, '; ')















                FROM song_singers AS ss















                LEFT JOIN artists ON artists.mid = ss.singer_mid















                WHERE ss.song_mid = s.mid















                ORDER BY ss.singer_order















            ) AS singers















        FROM songs AS s















        JOIN albums AS a ON a.mid = s.album_mid















        {where_sql}















        ORDER BY s.id, s.mid















    """















    return [dict(row) for row in connection.execute(sql, params).fetchall()]











def load_artist_song_mids(connection: sqlite3.Connection, artist_names: tuple[str, ...]) -> set[str]:



    if not artist_names:



        return set()



    placeholders = ", ".join("?" for _ in artist_names)



    rows = connection.execute(



        f"""



        SELECT DISTINCT song_singers.song_mid



        FROM song_singers



        JOIN artists ON artists.mid = song_singers.singer_mid



        WHERE artists.name IN ({placeholders})



        """,



        artist_names,



    ).fetchall()



    return {str(row["song_mid"]) for row in rows}











































def prepare_csv_rows(connection: sqlite3.Connection, rows: list[dict[str, Any]]) -> list[dict[str, Any]]:















    return prepare_song_csv_rows(connection, rows, include_credits=True)















































def write_csv(path: Path, rows: list[dict[str, Any]]) -> None:















    write_song_csv(path, rows, include_credits=True)















































def delete_songs(connection: sqlite3.Connection, song_mids: list[str]) -> None:















    if not song_mids:















        return















    connection.executemany("DELETE FROM songs WHERE mid = ?", [(mid,) for mid in song_mids])















































def dedupe_sort_key(row: dict[str, Any]) -> tuple[int, int, str]:















    album_type_rank = ALBUM_TYPE_PRIORITY.get(str(row.get("album_type") or ""), 999)















    try:















        song_id = int(row.get("song_id") or 0)















    except (TypeError, ValueError):















        song_id = 0















    return album_type_rank, song_id, str(row.get("song_mid") or "")















































def step12_dedupe_by_normalized_name(connection: sqlite3.Connection, rows: list[dict[str, Any]]) -> list[dict[str, Any]]:















    groups: dict[tuple[str, str, str], list[dict[str, Any]]] = defaultdict(list)







    for row in rows:















        row["normalized_name"] = normalize_song_name(str(row.get("song_name") or ""))















        row["lyricist_signature"] = credit_signature(connection, str(row["song_mid"]), "作词")







        row["composer_signature"] = credit_signature(connection, str(row["song_mid"]), "作曲")















        groups[(row["normalized_name"], row["lyricist_signature"], row["composer_signature"])].append(row)























    removed: list[dict[str, Any]] = []















    for (normalized_name, lyricist_key, composer_key), group in groups.items():







        if not normalized_name or not lyricist_key or not composer_key or len(group) <= 1:







            continue















        selected = min(group, key=dedupe_sort_key)















        for row in group:















            if row["song_mid"] == selected["song_mid"]:















                continue















            row["filter_step"] = "step12_same_credit_name_dedupe"







            row["filter_reason"] = "duplicate_normalized_name_and_same_lyricists_and_composers_prefer_recording_album_then_ep_then_single_then_lowest_id"







            row["selected_song_mid"] = selected["song_mid"]















            row["selected_song_id"] = selected["song_id"]















            row["selected_album_type"] = selected["album_type"]















            row["selected_name"] = selected["song_name"]















            removed.append(row)































    return sorted(removed, key=song_name_pinyin_sort_key)















































def credit_signature(connection: sqlite3.Connection, song_mid: str, role: str) -> str:





    rows = connection.execute(















        """















        SELECT artist_mid







        FROM song_credit_artists







        WHERE song_mid = ?







          AND role = ?







        """,













        (song_mid, role),







    ).fetchall()















    return "|".join(sorted({str(row["artist_mid"]) for row in rows}))





































def validate_identity_uniqueness(connection: sqlite3.Connection) -> dict[str, int]:















    duplicate_mid_rows = connection.execute(















        """















        SELECT COUNT(*)















        FROM (















            SELECT mid















            FROM songs















            GROUP BY mid















            HAVING COUNT(*) > 1















        )















        """















    ).fetchone()[0]















    duplicate_id_rows = connection.execute(















        """















        SELECT COUNT(*)















        FROM (















            SELECT id















            FROM songs















            GROUP BY id















            HAVING COUNT(*) > 1















        )















        """















    ).fetchone()[0]















    return {















        "duplicate_song_mid_groups": int(duplicate_mid_rows),















        "duplicate_song_id_groups": int(duplicate_id_rows),















    }















































def run(config: FilterConfig) -> dict[str, Any]:















    with connect(config.db_path) as connection:















        before_count = connection.execute("SELECT COUNT(*) FROM songs").fetchone()[0]















        identity_check_before = validate_identity_uniqueness(connection)











        all_rows = fetch_song_rows(connection)



        exported_temp_kept_rows = 0











        removed_by_name_dedupe = step12_dedupe_by_normalized_name(connection, all_rows)















        step12_removed_mids = {str(row["song_mid"]) for row in removed_by_name_dedupe}































        write_csv(config.name_dedupe_rejection_csv, prepare_csv_rows(connection, removed_by_name_dedupe))















        if config.temp_kept_csv is not None:



            temp_export_song_mids = load_artist_song_mids(connection, config.temp_export_artist_names)











            kept_rows = [















                dict(row)















                for row in all_rows















                if str(row["song_mid"]) not in step12_removed_mids



                and (not config.temp_export_artist_names or str(row["song_mid"]) in temp_export_song_mids)











            ]















            write_csv(config.temp_kept_csv, prepare_csv_rows(connection, kept_rows))



            exported_temp_kept_rows = len(kept_rows)



























        with connection:















            delete_songs(connection, sorted(step12_removed_mids))















            after_count = connection.execute("SELECT COUNT(*) FROM songs").fetchone()[0]















        identity_check_after = validate_identity_uniqueness(connection)















        song_singer_rows = connection.execute("SELECT COUNT(*) FROM song_singers").fetchone()[0]































    return {















        "db_path": config.db_path.as_posix(),















        "songs_before": before_count,















        "identity_check_before": identity_check_before,















        "step12_identity_dedupe": "skipped; songs.mid is PRIMARY KEY and songs.id is UNIQUE",















        "step12_removed_by_name_credit_dedupe": len(removed_by_name_dedupe),







        "songs_after_step12": after_count,















        "song_singer_rows_after_step12": song_singer_rows,















        "identity_check_after": identity_check_after,















        "name_dedupe_rejection_csv": config.name_dedupe_rejection_csv.as_posix(),















        "temp_kept_csv": config.temp_kept_csv.as_posix() if config.temp_kept_csv is not None else "",



        "temp_export_artist_names": list(config.temp_export_artist_names),



        "exported_temp_kept_rows": exported_temp_kept_rows,











    }















































def parse_args() -> argparse.Namespace:















    parser = argparse.ArgumentParser(description="Step 12: dedupe songs by normalized name and same lyricist/composer credits.")







    parser.add_argument("--db", type=Path, default=DEFAULT_DB_PATH)















    parser.add_argument("--name-dedupe-rejection-csv", type=Path, default=DEFAULT_NAME_DEDUPE_REJECTION_CSV)















    parser.add_argument("--temp-kept-csv", type=Path, default=DEFAULT_TEMP_KEPT_CSV)















    parser.add_argument("--no-temp-kept-csv", action="store_true", help="Do not write the temporary kept-song CSV view.")



    parser.add_argument(



        "--temp-export-artist-name",



        action="append",



        default=None,



        help="Singer name to include in the temporary kept-song CSV. Can be passed multiple times.",



    )



    parser.add_argument("--all-temp-kept-csv", action="store_true", help="Export all kept songs to the temporary kept-song CSV.")











    return parser.parse_args()















































def _main() -> None:















    ensure_utf8_stdout()















    args = parse_args()







    if args.all_temp_kept_csv:



        temp_export_artist_names: tuple[str, ...] = ()



    elif args.temp_export_artist_name is not None:



        temp_export_artist_names = tuple(args.temp_export_artist_name)



    else:



        temp_export_artist_names = DEFAULT_TEMP_EXPORT_ARTIST_NAMES











    result = run(















        FilterConfig(















            db_path=args.db,















            name_dedupe_rejection_csv=args.name_dedupe_rejection_csv,















            temp_kept_csv=None if args.no_temp_kept_csv else args.temp_kept_csv,



            temp_export_artist_names=temp_export_artist_names,











        )















    )















    print(json.dumps(result, ensure_ascii=False, indent=2))















































def main() -> None:
    run_with_log(Path(__file__).stem, _main)


if __name__ == "__main__":















    main()
















